## This script is written mainly for caching the inverse of a matrix rather than compute it repeatedly. 
## as matrix inversion is usually a costly computation.

## Creates a special "matrix" object that can cache its inverse.
makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL

        set <- function(y) {
                x <<- y
                inv <<- NULL
        }

        get <- function() x

        setinverse <- function(inverse) inv <<- inverse

        getinverse <- function() inv

        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
}

## Computes the inverse of the special "matrix" returned by makeCacheMatrix above. 
## If the inverse has already been calculated, then retrieve the inverse from the cache.
cacheSolve <- function(x, ...) {
        inv <- x$getinverse()

        if(!is.null(inv)) {
            message("getting cached data")
            return(inv)
        }

        data <- x$get()
        inv <- solve(data)
        x$setinverse(inv)
        inv
}